public class Lesson3VariablesAndPrimitives {

    public static void main(String[] args) {
        byte newByte = 127;
        short newShort = 32000;
        int newInt = 2000000000;

        long newLong = 20000000000L;
        System.out.println(newLong);

        float newFloat = 0.123f;
        double newDouble = 0.123d;

        char newChar = 'i';
        boolean newBoolean = true;


    }

}
