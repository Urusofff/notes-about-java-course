public class Lesson3PrimitiveTypes {

    public static void main(String[] args) {
        byte newByte = 127; // смените на 128
        short newShort = 32000; // смените на 33000
        int newInt = 2000000000; // смените на 3000000000

        long newLong = 20000000000L; // смените на 30000000000L
        System.out.println(newLong); // выведите переменную newInt вместо newLong

        float newFloat = 0.123f; // смените на 0.125f
        double newDouble = 0.123d; // смените на 1.123d

        char newChar = 'i'; // смените на 'Ai'
        boolean newBoolean = true; // смените на FALSE

    }
}